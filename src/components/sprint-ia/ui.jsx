export function ProgressRing({ pct, size = 48, sw = 3.5, color = "#10B981" }) {
  const radius = (size - sw) / 2;
  const circumference = 2 * Math.PI * radius;
  return (
    <svg width={size} height={size} className="sia-progress-ring">
      <circle cx={size / 2} cy={size / 2} r={radius} fill="none" stroke="#1a2438" strokeWidth={sw} />
      <circle
        cx={size / 2}
        cy={size / 2}
        r={radius}
        fill="none"
        stroke={color}
        strokeWidth={sw}
        strokeDasharray={circumference}
        strokeDashoffset={circumference - (pct / 100) * circumference}
        strokeLinecap="round"
        className="sia-progress-ring__value"
      />
    </svg>
  );
}

